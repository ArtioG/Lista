#include <stdlib.h>
#include <stdio.h>
#include <math.h>

int main ()
{
    int x,y;
 printf("Escreva o um valor para X: ");
 scanf("%d", &x);

 printf("Escreva um valor para y: ");
 scanf("%d", &y);

 if (x == y)
 {
  printf("Ambos são verdadeiros"); 
 }
 else if (x != y)
 {
  printf("Ambos são falsos");
 }
 else
 return 0;
} 

 
 
